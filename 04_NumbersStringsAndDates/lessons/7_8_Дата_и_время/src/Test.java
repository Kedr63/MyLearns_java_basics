        System.out.println(birthday.plusYears(18));  // 1994-12-31

        // –¢–∞–∫–∂–µ —ç—Ç–∏ –∫–ª–∞—Å—Å—ã –ø–æ–∑–≤–æ–ª—è—é—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Å –≤—Ä–µ–º–µ–Ω–Ω—ã–º–∏ –∑–æ–Ω–∞–º–∏, —Ç/–µ —Å —á–∞—Å–æ–≤—ã–º–∏ –ø–æ—è—Å–∞–º–∏
        // –î–æ–ø—É—Å—Ç–∏–º –º–æ–∂–µ–º –ø–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è –≤ –ù—å—é-–ô–æ—Ä–∫–µ
        LocalDateTime nowNY = LocalDateTime.now(ZoneId.of("America/New_York"));
        System.out.println(nowNY);  // 2021-10-26T07:56:04.871427

        // –ï—Å–ª–∏ —É –Ω–∞—Å –¥–∞—Ç–∞ –∏ –≤—Ä–µ–º—è —Å–æ–¥–µ—Ä–∂–∞—Ç—Å—è –≤ —Å—Ç—Ä–æ–∫–µ –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ, —Ç–æ —ç—Ç–∏ –∫–ª–∞—Å—Å—ã
        // –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è—é—Ç –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å —Ç–∞–∫—É—é —Å—Ç—Ä–æ–∫—É –≤ –æ–±—ä–µ–∫—Ç—ã —ç—Ç–∏—Ö –∫–ª–∞—Å—Å–æ–≤.
        // –¢–∞–∫–æ–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ, –∫–∞–∫ –∏ –ª—é–±–æ–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –ª—é–±–æ–≥–æ —Ç–µ–∫—Å—Ç–∞ –≤ –Ω–µ–∫–∏–π –æ–±—ä–µ–∫—Ç
        // –Ω–∞–∑—ã–≤–∞–µ—Ç—Å—è –ü–ê–†–°–ò–ù–ì–û–ú
        // –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å—Ç—å —Å—Ç—Ä–æ–∫–∞ —Å–æ–¥–µ—Ä–∂–∞—â–∞—è –¥–∞—Ç—É –∏ –º—ã —Ö–æ—Ç–∏–º –µ–µ –ø—Ä–µ–≤—Ä–∞—Ç–∏—Ç—å –≤ –æ–±—ä–µ–∫—Ç
        String date = "23/01/2021";
        // –¥–ª—è —ç—Ç–æ–≥–æ –Ω—É–∂–Ω–æ —Å–æ–∑–¥–∞—Ç—å –æ–±—ä–µ–∫—Ç –∫–ª–∞—Å—Å–∞ /DateTimeFormatter/ –∏ –ø–µ—Ä–µ–¥–∞—Ç—å —Å—é–¥–∞ —Ñ–æ—Ä–º–∞—Ç —ç—Ç–æ–π –¥–∞—Ç—ã
