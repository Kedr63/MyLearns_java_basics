        // –∏ –≤–æ—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è —ç—Ç–∏–º /formatter/-–æ–º
        LocalDate localDate = LocalDate.parse(date, formatter);
        System.out.println(localDate);  // 2021-01-23

        // –ú–µ—Ç–æ–¥—ã –ø–∞—Ä—Å–∏–Ω–≥–∞, –∫–ª–∞—Å—Å–æ–≤ LocalDate –∏ LocalDateTime, —ç—Ç–æ –æ—á–µ–Ω—å —É–¥–æ–±–Ω—ã–π –∏–Ω—Å—Ç—Ä—É–º–µ–Ω—Ç,
        // –ø–æ—Å–∫–æ–ª—å–∫—É –æ–Ω–∏ –ø—Ä–∏ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–∏ —Å—Ç—Ä–æ–∫–∏ –≤ –¥–∞—Ç—É –ø—Ä–æ–≤–µ—Ä—è—é—Ç –≤—Å–µ —Ç–æ–Ω–∫–æ—Å—Ç–∏ —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Å
        // –∫–∞–ª–µ–Ω–¥–∞—Ä–µ–º, –Ω–∞–ø—Ä–∏–º–µ—Ä —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–π –¥–∞—Ç—ã, –Ω–∞–ø—Ä–∏–º–µ—Ä 29 —Ñ–µ–≤—Ä–∞–ª—è –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—ã–π
        // –≥–æ–¥, –µ—Å–ª–∏ –≤ —Å—Ç—Ä–æ–∫—É –∑–∞–ø–∏—Å–∞—Ç—å 29/02/2021 (–∞ —Ç–∞–∫–æ–π –¥–∞—Ç—ã –Ω–µ—Ç –≤ 2021 –≥–æ–¥—É), —Ç–æ —ç—Ç–∞ –¥–∞—Ç–∞
        // –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç—Å—è –≤ 28/02/2021.

        // –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ —Ç–æ–º—É –∫–∞–∫ –º—ã –º–æ–∂–µ–º –ø—Ä–æ–ø–∞—Ä—Å–∏—Ç—å —Å—Ç—Ä–æ–∫—É —Å –¥–∞—Ç–∞–º–∏ –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ –≤ –æ–±—ä–µ–∫—Ç,
        // –º—ã –º–æ–∂–µ–º —Å–¥–µ–ª–∞—Ç—å –∏ –æ–±—Ä–∞—Ç–Ω—É—é –æ–ø–µ—Ä–∞—Ü–∏—é, —Ç/–µ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞—Ç—å –æ–±—ä–µ–∫—Ç –≤ —Å—Ç—Ä–æ–∫—É —Å –¥–∞—Ç–æ–π –∏
        // –≤—Ä–µ–º–µ–Ω–µ–º –≤ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω–æ–º —Ñ–æ—Ä–º–∞—Ç–µ. –≠—Ç–æ –º–æ–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å —Å –ø–æ–º–æ—â—å—é —Ç–æ–≥–æ –∂–µ —Ñ–æ—Ä–º–∞—Ç–æ—Ä–∞
        DateTimeFormatter formatter1 = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        LocalDateTime now2 = LocalDateTime.now();
        System.out.println(formatter1.format(now2));  // 26/10/2021 15:27:08


        // –§–æ—Ä–º–∞—Ç –¥–∞—Ç—ã –∏ –≤—Ä–µ–º–µ–Ω–∏ –ø—Ä–∏–Ω—è—Ç–æ –≤—ã–Ω–æ—Å–∏—Ç—å –≤ –æ—Ç–¥–µ–ª—å–Ω—É—é –∫–æ–Ω—Å—Ç–∞–Ω—Ç—É. –ß—Ç–æ–±—ã —É–ø—Ä–æ—Å—Ç–∏—Ç—å –∂–∏–∑–Ω—å
        // —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–∞–º, –ø—Ä–∏–¥—É–º–∞–ª–∏ –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø—Ä–µ–¥—É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã—Ö –∫–æ–Ω—Å—Ç–∞–Ω—Ç,–∫–æ—Ç–æ—Ä—ã–µ –º–æ–∂–Ω–æ –ø–µ—Ä–µ–¥–∞—Ç—å
        // —Å –ø–æ–º–æ—â—å—é –º–µ—Ç–æ–¥–∞ /ofLocalizedDate/ –∏–ª–∏ /ofLocalizedDateTime/
