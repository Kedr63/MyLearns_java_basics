        // –ù–æ —Å—É—â–µ—Å—Ç–≤—É—é—Ç –±–æ–ª–µ–µ —É–¥–æ–±–Ω—ã–µ –∏ —Å–æ–≤—Ä–µ–º–µ–Ω–Ω—ã–µ –∫–ª–∞—Å—Å—ã –∫–æ—Ç–æ—Ä—ã–µ –ø–æ—è–≤–∏–ª–∏—Å—å –≤ 8 Java:
        //‚ù§Ô∏è LocalDate   - –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Å –¥–∞—Ç–æ–π
        //‚ù§Ô∏è LocalDateTime  - –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º

        // –ï—Å—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å–ø–æ—Å–æ–±–æ–≤ —Å–æ–∑–¥–∞–Ω–∏—è –æ–±—ä–µ–∫—Ç–æ–≤ —ç—Ç–∏—Ö –∫–ª–∞—Å—Å–æ–≤ —Å–æ–¥–µ—Ä–∂–∞—â–∏—Ö –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–Ω—É—é –Ω—É–∂–Ω—É—é –Ω–∞–º –¥–∞—Ç—É
        // –∏–ª–∏ –¥–∞—Ç—É –∏ –≤—Ä–µ–º—è.
        // –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –Ω—É–∂–Ω–æ —Å–æ–∑–¥–∞—Ç—å —Å–µ–≥–æ–¥–Ω—è—à–Ω–∏–π –¥–µ–Ω—å
        LocalDate today = LocalDate.now();
        System.out.println(today);    // 2021-10-26

        // –ï—Å–ª–∏ –Ω—É–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è
        LocalDateTime now1 = LocalDateTime.now();
