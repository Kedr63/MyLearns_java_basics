        // –≤—Ç–æ—Ä–∞—è –¥–∞—Ç–∞ ‚ûî /—á–∏—Å–ª–æ –ø–æ–ª–æ–∂–∏—Ç–µ–ª—å–Ω–æ–µ/, –µ—Å–ª–∏ –Ω–∞–æ–±–æ—Ä–æ—Ç ‚ûî /—á–∏—Å–ª–æ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ–µ/
        System.out.println(time1.compareTo(time2));  // 2


        // üìå –í—ã—á–∏—Å–ª–µ–Ω–∏–µ —Ä–∞–∑–Ω–∏—Ü—ã –º/—É –¥–∞—Ç–∞–º–∏ –∏–ª–∏ –¥–∞—Ç–æ–π –∏ –≤—Ä–µ–º–µ–Ω–µ–º__________________________________________

        LocalDateTime time3 = LocalDateTime.now();
