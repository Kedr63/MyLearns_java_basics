        // –ï—Å–ª–∏ —Ö–æ—Ç–∏–º –∏–∑–º–µ–Ω–∏—Ç—å –Ω–∞ –∞–º–µ—Ä–∏–∫–∞–Ω—Å–∫–∏–π —Ñ–æ—Ä–º–∞—Ç:
