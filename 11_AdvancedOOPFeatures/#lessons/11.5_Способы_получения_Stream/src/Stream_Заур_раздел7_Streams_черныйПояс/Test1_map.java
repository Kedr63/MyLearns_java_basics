        // —Å–æ–¥–µ—Ä–∂–∏—Ç /String-–∏/ –º—ã –ø–æ–ª—É—á–∏–ª–∏ —Å—Ç—Ä–∏–º, –∫–æ—Ç–æ—Ä—ã–π —Å–æ–¥–µ—Ä–∂–∏—Ç /Integer-—ã/, —Ç/–µ –∏—Ö –¥–ª–∏–Ω—ã.

